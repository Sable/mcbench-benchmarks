function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

chrv000=get(handles.edit3,'string');
chrv001=get(handles.edit5,'string');
chrv002=get(handles.edit6,'string');
chrv003=get(handles.edit7,'string');
chrv004=get(handles.edit12,'string');
chrv005=get(handles.edit11,'string');
chrv006=get(handles.edit12,'string');
intv000=str2num(chrv004);
intv001=str2num(chrv005);
intv002=str2num(chrv006);
T=str2num(chrv003);
W=2*pi/T;
t=-T:T/intv000:T;
y=t*0;
for k=intv001:1:intv002
    if abs(mod(k,2))==0
        if abs(k)==0
            C0=eval(chrv000);
            y=y+C0;
        else
            Cke=eval(chrv001);
            y=y+Cke.*exp(complex(0,W.*k.*t));
        end
    else
        Cko=eval(chrv002);
        y=y+Cko.*exp(complex(0,W.*k.*t));
    end
end
plot3(t,real(y),imag(y));
view(2);
grid on;
xlabel('X-Axis');
ylabel('Y-Axis');
zlabel('Z-Axis');

set(handles.edit8,'string','0');
set(handles.edit9,'string','90');
set(handles.slider5,'value',0);
set(handles.slider4,'value',.25);






